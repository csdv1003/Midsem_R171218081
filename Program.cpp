//Creating a Virtual calculator for 2 and 5 with help of classes:
#include<iostream>
using namespace std;
class Sum{

        public:
                int i,k;
        void getdata(int x,int y){
                i = x;
                k = y;
        }
        int putdata(){
		return (i+k);
	}
};
int main(){

	Sum s;
	s.getdata(2,5);
	cout<<"The sum of 2 and 5 is: "<<s.putdata();
	return 0;
}
